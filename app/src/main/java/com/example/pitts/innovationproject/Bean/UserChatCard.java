package com.example.pitts.innovationproject.Bean;

public class UserChatCard {

    private String userChatUserIconUrl;
    private String userChatUserName;
    private String userChatContext;
    private String userChatTime;

    public String getUserChatUserIconUrl() {
        return userChatUserIconUrl;
    }

    public void setUserChatUserIconUrl(String userChatUserIconUrl) {
        this.userChatUserIconUrl = userChatUserIconUrl;
    }

    public String getUserChatUserName() {
        return userChatUserName;
    }

    public void setUserChatUserName(String userChatUserName) {
        this.userChatUserName = userChatUserName;
    }

    public String getUserChatContext() {
        return userChatContext;
    }

    public void setUserChatContext(String userChatContext) {
        this.userChatContext = userChatContext;
    }

    public String getUserChatTime() {
        return userChatTime;
    }

    public void setUserChatTime(String userChatTime) {
        this.userChatTime = userChatTime;
    }
}
